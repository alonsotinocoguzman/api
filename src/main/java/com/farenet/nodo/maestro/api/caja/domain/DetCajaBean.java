    public void setCategoria_key(String categoria_key) {
        this.categoria_key = categoria_key;
    }

    public String getTipoinspeccion_key() {
        return tipoinspeccion_key;
    }

    public void setTipoinspeccion_key(String tipoinspeccion_key) {
        this.tipoinspeccion_key = tipoinspeccion_key;
    }

